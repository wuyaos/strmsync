// Package db 提供数据库配置和连接管理
//
// 本包负责数据库的初始化、连接池管理和配置加载。
//
// # 主要功能
//
//   - 从环境变量加载数据库配置
//   - 创建和管理数据库连接（单例模式）
//   - 自动执行数据库迁移
//   - 提供事务支持
//
// # 配置项
//
//   - DB_PATH: SQLite 数据库文件路径
//
// # 使用示例
//
//	cfg, err := db.LoadFromEnv()
//	db, err := db.GetDB()
//
// # 依赖关系
//
//	infra/db 不依赖 app 层
//	app 层服务通过依赖注入获取 *gorm.DB
package db
